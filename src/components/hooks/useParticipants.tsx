import {useState} from 'react';
import {IParticipant} from '../../models/models';

function useParticipants(initialVal: Array<IParticipant>) {
  const [participants, setParticipants] = useState(initialVal);
  function setPaid(index: number): void {
    let temp = [...participants];

    temp[index].paid = !temp[index].paid;
    console.log(temp[index].paid);
    setParticipants(temp);
    // console.log(temp);
  }
  return {participants, setPaid};
}
export default useParticipants;
